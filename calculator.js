/**
 * Calculator module
 * @module calculator
 */

/**
 * add two numbers
 * @param {number} n1 -first number
 * @param {number} n2 -second number
 * @returns {number} -sum of n1 and n2
 */
exports.add = (n1, n2) => {
  return n1 + n2;
};

/**
 * multiply two numbers
 * @param {number}n3 -first number
 * @param {number}n4 -second number
 * @returns {number} -multiplication of n1 and n2
 */
exports.multiply = (n3, n4) => {
  return n3 * n4;
};
